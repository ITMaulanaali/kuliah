//Variable

//Mutable variable (type data bisa diubah)
var

//Imutable variable (type data tidak bisa diubah)
val

//Mendefinisikan type data variable secara implisit
var namaVariable = 30 //otomatis jadi int

//Menginisialisasi type data variable secara eskplisit
var namaVariable: Int = 30

//membuat variable dengan nilai null(by default variable di kotlin tidak boleh >
var namaVariable: Int? = null

//variable Constant untuk keperluan global biasanya
const val namaVariable: String = "hai"
