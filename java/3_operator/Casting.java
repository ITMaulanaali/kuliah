
class Casting {

    public static void main(String[] args){
        float a = 3.64f;
        double b = 2.75;

        double hasil = a+b;
        System.out.println("Hasil casting ke int adalah: " + (int)hasil);

    }

}

/*
 * casting akan merubah variable dengan satu type data untuk dirubah ke type data lain.
 */