class Decrement {
    
    public static void main(String[] args) {
        int a = 5;
        int hasil;

        hasil = a--;
        System.out.println("Hasil variable a=5 decrement a--, simpan ke hasil: " + hasil + " nilai a: " + a);

        a = 5;
        hasil = --a;
        System.out.println("Hasil variable a=5 decrement --a, simpan ke hasil: " + hasil + " nilai a: " + a);

    }
}
