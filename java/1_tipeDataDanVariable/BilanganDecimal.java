class BilanganDecimal{
    
    public static void main(String[] args){
        float f = 10.24f;
        double d = 11.35;

        System.out.printf("Float f bernilai:    %f dengan range: %d Byte",f, Float.BYTES); System.out.println();
        System.out.printf("Double d bernilai:   %f dengan range: %d Byte",d, Double.BYTES); System.out.println();
    }
}
