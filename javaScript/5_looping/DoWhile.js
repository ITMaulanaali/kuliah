let a = 5;
let iterasi = 0;

do{
    a++;
    iterasi++;
    console.log("Iterasi sebanyak: " + iterasi + " nilai a= " + a);
}while(a<=10);


/*
Pada DoWhile itu diberikan syarat boolean true atau false yang bisa didapat dari mana saja tidak terkecuali seperti contoh diatas adalah hasil boolean dari komparasi

DoWhile akan mengeksekusi setidaknya satu kali kemudian baru mengecek syaratnya apakah masih terpenuhi untuk dilakukan looping kembali.
*/