let a = 5;
let iterasi = 0;

while(a<=10){
    a++;
    iterasi++;
    console.log("Iterasi sebanyak: " + iterasi + " nilai a= " + a);
}

/*
Pada while itu diberikan syarat nilai boolean true atau false yang bisa didapat dari mana saja tidak terkecuali seperti contoh diatas adalah hasil boolean dari komparasi
Namun yang umum biasa disebut adalah syarat. Ketika syarat terpenuhi maka akan dilakukan aksi(blok kode didalam while yang akan di iterasi)

*/