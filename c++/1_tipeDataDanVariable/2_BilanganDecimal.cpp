#include <iostream>
using namespace std;

int main(){

    float f;
    double d;

    cout << "Float  " << sizeof(f) << "Byte" << endl;
    cout << "Double " << sizeof(d) << "Byte" << endl;
    cin.get();
    return 0;
}

/*
Tipe data float dan double adalah tipe data yang dapat menampung bilangan atau nilai berkoma(,)
float   4Byte
double  8Byte

*/